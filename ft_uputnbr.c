#include "ft_printf.h"

void	ft_uputnbr(unsigned int n)
{
	if (n > 9)
	{
		ft_uputnbr(n / 10);
		ft_uputnbr(n % 10);
	}
	else
	{
		n = n + 48;
		write(1, &n, 1);
	}
}